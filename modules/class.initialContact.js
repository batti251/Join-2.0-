class InitialContact {
  name;
  email;
  phone;
  checkbox;
  password;
  canLogin;

  constructor(name, email, phone, checkbox, password, canLogin) {
    this.name = name;
    this.email = email;
    this.phone = phone;
    this.checkbox = checkbox;
    this.password = password;
    this.canLogin = canLogin
  }
}
